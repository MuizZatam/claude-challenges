# Using std print function
print("Hello, World!")

# Using f-strings
my_str = "Hello, World!"
print(f"{my_str}")

# Using the format method
print("{}, {}{}".format("Hello", "World", "!"))

# Using % formatting
# Note: Method not recommended
print("%s, %s%s" % ("Hello", "World", "!"))

# Using str concatenation
print("Hello, " + "World" + "!")